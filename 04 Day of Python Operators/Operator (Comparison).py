#Comparison Operators
"""
==: Same
!=: Not same
>: Greater than
<: Less than
>=: Greater than or Equal
<=: Less than or equal
"""
#Example Same (answers will be True or False)

x = 10
y = 10

print(x == y)

#Example not same 

x = 10
y = 10

print(x != y)

#Example greater than 

x = 10
y = 10

print(x > y)


#Example greater than 

x = 10
y = 10

print(x < y)

#Example greater than or equal

x = 10
y = 10

print(x >= y)

#Example less than or equal

x = 10
y = 10

print(x <= y)
