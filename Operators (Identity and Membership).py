#Identity Operators
"""
Identity operators are: IS or IS NOT
and the result will be true or false
"""
#Example (IS)

x = 2 
y = 5

print ( x is y)

#Example (IS NOT)

x = 2 
y = 5

print ( x is not y)

#Membership 
"""
Membership operators are: IN, NOT IN
Results will be true or false 
"""
#Example (IN)

x = ["Canbe", "Moos", "Qare"]
y= ["Qare"]

print(y in x)

#Example (IN NOT)

x = ["Canbe", "Moos", "Qare"]
y= ["Qare"]

print(y not in x)